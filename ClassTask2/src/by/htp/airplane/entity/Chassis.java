package by.htp.airplane.entity;

public class Chassis {

	int chassisWeight; 
	String chassisType;
	int chassisSize;
	
	public int getChassisWeight() {
		return chassisWeight;
	}
	public void setChassisWeight(int chassisWeight) {
		this.chassisWeight = chassisWeight;
	}
	public String getChassisType() {
		return chassisType;
	}
	public void setChassisType(String chassisType) {
		this.chassisType = chassisType;
	}
	public int getChassisSize() {
		return chassisSize;
	}
	public void setChassisSize(int chassisSize) {
		this.chassisSize = chassisSize;
	}
	
	
}
